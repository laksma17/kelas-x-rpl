let a = true;
let b = false;
let c = true;
let d = false;

// OR bernilai true jika salah satu true
console.log(d || b || a);

// AND bernilai false jika salah satu false
console.log(a && b && c);

let x = 7;
let y = 6;

// Lebih besar
console.log(x > y);

// Lebih kecil
console.log(x < y);

// Sama dengan
console.log(x == y);

// Lebih besar sama dengan
console.log(x >= y);

// Lebih kecil sama dengan
console.log(x <= y);

